CREATE TABLE SAMPLE_BOARD 
(
  B_NO NUMBER NOT NULL 
, B_NICK VARCHAR2(20) NOT NULL 
, B_PW VARCHAR2(64) NOT NULL 
, B_TEXT VARCHAR2(4000) NOT NULL 
, B_DATE DATE DEFAULT sysdate NOT NULL 
, CONSTRAINT BOARD_PK PRIMARY KEY 
  (
    B_NO 
  )
  ENABLE 
);

COMMENT ON COLUMN SAMPLE_BOARD.B_NO IS '번호';

COMMENT ON COLUMN SAMPLE_BOARD.B_NICK IS '닉네임';

COMMENT ON COLUMN SAMPLE_BOARD.B_PW IS '비번';

COMMENT ON COLUMN SAMPLE_BOARD.B_TEXT IS '내용';

COMMENT ON COLUMN SAMPLE_BOARD.B_DATE IS '등록날자';

-- Sequence
CREATE SEQUENCE DEFULT.SAMPLE_BOARD_SEQ_NO
START WITH 1
  MAXVALUE 999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  CACHE 20
  NOORDER;

-- Trigger
CREATE TRIGGER DEFULT.SAMPLE_BOARD_TRG_NO
BEFORE INSERT
ON DEFULT.SAMPLE_BOARD
REFERENCING NEW AS New OLD AS Old
FOR EACH ROW
BEGIN
-- For Toad:  Highlight column B_NO
  :new.B_NO := SAMPLE_BOARD_SEQ_NO.nextval;
END SAMPLE_BOARD_TRG_NO;
/
